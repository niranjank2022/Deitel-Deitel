/* Q. NO. 3.2
Write four different C statements that each add 1 to integer varisble x 
*/
#include <stdio.h>

int main() {
    int x;

    /* 1 Adding and storing */
    x = x + 1;

    /* 2 Assignment Operstor */
    x += 1;

    /* 3 Pre-increment */
    ++x;

    /* 4 Post-increment */
    x++;
}